# Dictionary methods
# Dictionary is a collection of key-value pairs enclosed by a curly bracket

# >> Creating Dictionaries With {} And Dict()

dict1 = {1:'one',2:'two',3:'three'}
dict2 = dict()   
dict3 = dict({1:'one',2:'two'})

print(dict1) # {1: 'one', 2: 'two', 3: 'three'}
print(dict2) # {}
print(dict3) # {1: 'one', 2: 'two'}

